nome = "Nicolas"
sobrenome = "Yoshizawa"

# Utilizando a adição de strings
puts "Meu nome é " + nome + " " + sobrenome

# Utilizando a interpolação de strings
puts "Meu nome é #{nome} #{sobrenome}"

# Exemplos de interpolação com expressões
puts "2 + 2 = #{2 + 2}"

# OBS: A interpolação de strings só funciona com aspas duplas (" ").