# É maior?
1 > 2 # falso

# É menor?
1 < 2 # verdadeiro

# É maior ou igual?
1 >= 1 # verdadeiro

# É menor ou igual?
1 <= 2 # verdadeiro

# É igual?
1 == 1 # verdadeiro
1 == 2 # falso

# É diferente?
1 != 2 # verdadeiro
1 != 1 # falso

# SpaceShip Operator (Nave espacial)
# Retorna -1 se o valor da esquerda for menor, 0 se forem iguais e 1 se o valor da esquerda for maior
1 <=> 2 # Retorna -1
2 <=> 2 # Retorna 0
3 <=> 2 # Retorna 1

